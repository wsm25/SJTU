int cnt[MAX+1]={0};

inline unsigned readuint() {
    int x=0;
    char ch=getchar();
    while(ch<'0'||ch>'9') ch=getchar();
    while(ch>='0'&&ch<='9') x=x*10+ch-'0', ch=getchar();
    return x;
}

int main(){
    int n=readuint();
    while(n--){
        cnt[readuint()]++;
    }
    for(int i=1; i<=MAX; i++){
        unsigned n=mapping[i];
        if(cnt[n]==0) continue;
        char buf[4];
        int j=3, x=n;
        while(x!=0){
            buf[j--]=x%10;
            x=x/10;
        }
        while(cnt[n]--){
            int k=j;
            while((++k)<4) putchar('0'+buf[k]);
            putchar(' ');
        }
    }
}